
// flutter 常用的命令

// 查看flutter sdk分支
// flutter channel

// 分析代码
// flutter analyze

// 指定语言：默认Android使用Java，iOS使用Objective-C
// flutter create -i swift -a kotlin flutter_app

// 创建混编项目
// flutter create -t module flutter_module


// 清除依赖包
// flutter clean

// 下载依赖包
// flutter pub get

// 获取项目所有的依赖包
// flutter packages get

// 更新依赖包至最新版本
// flutter packages upgrade

// 获取模拟器列表
// flutter emulators

// 获取真机列表，或模拟器列表
// flutter devices

// 运行指定模拟器或者真机
// flutter run -d <deviceId>

// 安卓打包
// flutter build apk

// ios打包
// flutter build ios